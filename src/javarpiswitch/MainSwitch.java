    private void mnuItemGroundFloorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_mnuItemGroundFloorActionPerformed
        // TODO add your handling code here:
        
      
        if(pc ==null)
        {
            pc  = new PortController();
            System.out.println("Creating new controller class");
            pc.SetFloor(PortController.GROUND_FLOOR, this) ;
            this.setLabel("Ground Floor Active", 0) ;
        }
       else if (pc !=null) {
             System.out.println("First Floor set correctly");
             this.setLabel("Ground Floor Active", 0) ;
            }
    }//GEN-LAST:event_mnuItemGroundFloorActionPerformed

    public void setActionsLights(int swnum , JButton fbutton, JLabel m_label)
    {
        if(pc !=null)
         {
        
        if(fbutton.getText().trim().equals("ON"))
             
             
             
             
               {
               fbutton.setText("OFF") ;
               pc.setLight(swnum, true) ;
               m_label.setIcon(this.getlabelOnIcon());
               }else 
                    {
                      fbutton.setText("ON");
                        pc.setLight(swnum, false) ;
                        m_label.setIcon(this.getlabelOffIcon());
                        
                    }
         }
         else 
         {
            System.out.println("Controller has not been instantiated");
         }
    }
    
