    public void setLabel(String floor, int level)
    {
      
        switch(level){
            case 0: // ground floor 
                labelFloorGround.setText(floor);
                labelFloorGround.setForeground(Color.BLUE);
                this.gndActiveIcon.setIcon(this.getlabelOnIcon());
                
                break ;
            case 1: 
                 
                labelFloorSecond.setText(floor);
                labelFloorSecond.setForeground(Color.BLUE);
                this.istFloorActiveIcon.setIcon(this.getlabelOnIcon());
                break ;
            case 2:
                
                
                break ;
                
            default :
               
                
                
        }
        
    }
    
